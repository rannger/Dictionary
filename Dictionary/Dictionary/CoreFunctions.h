#import <Foundation/Foundation.h>

/**
 *  if obj is a string and it's not a empty string,IsStringWithAnyText will return true
 *
 *  @param obj an object
 *
 *  @return if obj is a string and it's not a empty string,IsStringWithAnyText will return true,else false
 */
BOOL IsStringWithAnyText(id obj);

/**
 *  if obj is a array and it's not a empty array,IsArrayWithAnyObj will return true
 *
 *  @param obj an object
 *
 *  @return if obj is a string and it's not a empty string,IsStringWithAnyText will return true,else false
 */
BOOL IsArrayWithAnyObj(id obj);

